    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        Cliente[] aux = new Cliente[ListaCliente.getLista().length - 1];
        int row = jTable2.getSelectedRow();
        System.out.println("Fila = " + row);
        System.out.println("Value = " + jTable2.getModel().getValueAt(row, 0));
        String strIdx = (String) jTable2.getModel().getValueAt(row, 0);
        System.out.println("String = " + strIdx);
        if (strIdx != null && !strIdx.isEmpty()) {
            int indice = Integer.parseInt(strIdx);
            ListaCliente.del(indice);

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        }
        if (ListaCliente.getLista()[row] != null) {
         ListaCliente.getLista()[row] = null;
         }
         for (int i = 0; i < ListaCliente.getLista().length; i++ ) {
         if (ListaCliente.getLista()[i] !=null) {
         for (int j = 0; j < aux.length; j++) {
         if (aux[j] == null) {
         aux[j] = ListaCliente.getLista()[i];
         break;
         }
         }
         }
         }
         ListaCliente.setLista(aux);
         ListaCliente.saveLista();
                
         
        
        refreshTable();

    }//GEN-LAST:event_jButton5ActionPerformed

